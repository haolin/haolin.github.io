//起点


//======================================================================================================================
cc.Obj_Begin = cc.EmptyObj.extend({

    //------------------------------------------------------------------------------------------------------------------
    ctor: function(block)
    {
        this._super();
        this.m_IsBlock = block && block > 0;
    },

    //------------------------------------------------------------------------------------------------------------------
    description: function()
    {
        return "Obj_Begin";
    },

    //------------------------------------------------------------------------------------------------------------------
    toString: function()
    {
        var pos = this.getParentNode().getGridPos();
        return this.description() + " (" + pos.x + ", " + pos.y + ")";
    },

    //------------------------------------------------------------------------------------------------------------------
    isBlock: function()
    {
        return this.m_IsBlock;
    }
});

//工厂方法
cc.Obj_Begin.create = function(block)
{
    return new cc.Obj_Begin(block);
};