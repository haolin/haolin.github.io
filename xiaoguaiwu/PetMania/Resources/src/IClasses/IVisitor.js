//======================================================================================================================
cc.IVisitor = cc.Class.extend({

    //--------------------------------------------------------------------------------------------------
    ctor: function()
    {
        this.m_Elements = [];
        return this;
    },

    //--------------------------------------------------------------------------------------------------
    getElements: function()
    {
        return this.m_Elements;
    },

    //--------------------------------------------------------------------------------------------------
    visit: function(visElement)
    {
        this.m_Elements.push(visElement);
        return this;
    },

    //--------------------------------------------------------------------------------------------------
    visitFinish: function()
    {
        return this;
    }
});

//工厂方法

cc.IVisitor.create = function()
{
    return new cc.IVisitor();
};
